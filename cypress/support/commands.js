// -- This is a parent command --
Cypress.Commands.add('login', () => {
  cy.visit('http://localhost:3000')
  cy.url().should('include', '/auth')

  cy.get('button')
    .click()
    .should(() => {
      assert.isNotNull(localStorage.getItem('vuex'), 'is not null')
    })
  cy.url().should('include', '/browse/genres')
})
